import { describe, it, expect } from 'vitest'

describe('Basic Math Operations', () => {
  it('adds 1 + 2 to equal 3', () => {
    expect(1 + 2).toBe(3)
  })

  it('multiplies 2 * 3 to equal 6', () => {
    expect(2 * 3).toBe(6)
  })

  it('divides 10 / 2 to equal 5', () => {
    expect(10 / 2).toBe(5)
  })
})